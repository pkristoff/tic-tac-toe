server <- function(input, output, session) {
  xSide <- -1
  oSide <- 1
  humanSide <- 0
  computerSide <- 0
  xWins <- 0
  oWins <- 0
  SetHumanSide <- function(board) {
    if (length(which(board == xSide)) > length(which(board == oSide))) {
      humanSide <<- xSide
      computerSide <<- oSide
    } else {
      humanSide <<- oSide
      computerSide <<- xSide
