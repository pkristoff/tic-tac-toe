    for (index in 1:9) {
      cells = UICellForIndex(index)
      if (localBoard[index] != 0) {
        shinyjs::disable(cells[[1]])
        shinyjs::disable(cells[[2]])
      }
      if (computerSide == xSide){
        shinyjs::disable(cells[[1]])
      } else if (computerSide == oSide){
        shinyjs::disable(cells[[2]])
      }
    }
  }

  ResetGame <- function(){
    print("ResetGame")
    DisableWholeBoard(localBoard, FALSE)
    for(index in 1:9){
