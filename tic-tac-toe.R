IsMachineTurn <- function (board) {
  numX <- 0
  numY <- 0
  for (rowNum in 1:3) {
    for (colNum in 1:3) {
      if (board[rowNum, colNum] == 1) {
        numX <- numX + 1
      } else if (board[rowNum, colNum]) {
        numY <- numY + 1
      }
    }
  }
  numX != numY
}

